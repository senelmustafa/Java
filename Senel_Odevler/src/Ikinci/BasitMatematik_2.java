package Ikinci;

public class BasitMatematik_2 {

	public static void main(String[] args) {
		double x = 100.5;
		int y = 25;
		System.out.println("x + y = " + (x + y));
		System.out.println("x - y = " + (x - y));
		System.out.println("x * y = " + (x * y));
		System.out.println("x / y = " + (x / y));
		System.out.println("x % y = " + (x % y));

	}

}
